package components;

public class logicalOpeartors {
  public static void main(String[] args) {
    // &&, ||, !
    if (true && true) {
      System.out.println("true && true");
    }
    if (true || false) {
      System.out.println("true || false"); 
    }
    if (!false) {
      System.out.println("!false");
    }
  }
}
